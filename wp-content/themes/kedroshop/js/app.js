$('#burger_mobile').on('click', '.side-page__link', function(e) {
    e.preventDefault();

    if(this.nextElementSibling) {
        $('.submenu').show();
        $('#burger_mobile').hide();
        $('#' + $(this).parent().attr('id')).clone().appendTo(".submenu");
        $('.submenu > li > a').prepend('<i class="fa fa fa-long-arrow-left"></i> ');
    } else 
        window.location.href = this.href;
})

